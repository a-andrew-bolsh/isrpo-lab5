from math import sqrt

def area(a, b, c):
    """
    Вычисляет площадь треугольника по формуле Герона
    
    Формула Герона: S = √(p(p-a)(p-b)(p-c)), где p - полупериметр
    
    Параметры:
        a (float): длина первой стороны треугольника
        b (float): длина второй стороны треугольника
        c (float): длина третьей стороны треугольника
    
    Возвращаемое значение:
        float: площадь треугольника
    
    Примечание:
        Для корректного вычисления стороны должны образовывать 
        валидный треугольник (сумма любых двух сторон больше третьей)
    """
   
    p = perimeter(a, b, c) / 2
    return sqrt(p * (p - a) * (p - b) * (p - c))


def perimeter(a, b, c):
    """
    Вычисляет периметр треугольника
    
    Периметр треугольника равен сумме длин всех его сторон: P = a + b + c
    
    Параметры:
        a (float): длина первой стороны треугольника
        b (float): длина второй стороны треугольника
        c (float): длина третьей стороны треугольника
    
    Возвращаемое значение:
        float: периметр треугольника
    """
    
    return a + b + c
